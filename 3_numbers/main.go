package main

import (
	"fmt"
	"math"
)

func main() {
	fmt.Println("Addition:", 1+3)
	fmt.Println("Substraction:", 5-4)
	fmt.Println("Multiplication:", 6*7)
	fmt.Println("Division:", 100.0/3)          //that's how we define float variables in Go
	fmt.Println("Exponents:", math.Pow(15, 3)) //Using power fuction of math package
}
